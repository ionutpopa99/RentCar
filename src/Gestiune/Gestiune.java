        public ArrayList<Date> getProductList()
    {
            ArrayList<Date> productList  = new ArrayList<Date>();
            Connection con = getConnection();
            String query = "SELECT * FROM datepersonale";
            
            Statement st;
            ResultSet rs;
            
        try {
            
            st = con.createStatement();
            rs = st.executeQuery(query);
            Date product;
            
            while(rs.next())
            {
                product = new Date(rs.getString("nume"),rs.getString("prenume"),rs.getString("oras"),rs.getInt("numar_de_telefon"),rs.getString("email"),rs.getInt("varsta"),rs.getString("nume_card"),rs.getInt("numar_card"),rs.getInt("cvc"),rs.getString("exp_card"));
                productList.add(product);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Date.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return productList; 
                
    }
    
     public void Show_Products_In_JTable()
    {
        ArrayList<Date> list = getProductList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        // clear jtable content
        model.setRowCount(0);
        Object[] row = new Object[10];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getNume();
            row[1] = list.get(i).getPrenume();
            row[2] = list.get(i).getOras();
            row[3] = list.get(i).getNumar_de_telefon();
            row[4] = list.get(i).getEmail();
            row[5] = list.get(i).getVarsta();
            row[6] = list.get(i).getNume_card();
            row[7] = list.get(i).getNumar_card();
            row[8] = list.get(i).getCvc();
            row[9] = list.get(i).getExp_card();
            model.addRow(row);
        }
    
    }
    
    
