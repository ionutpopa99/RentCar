      public Connection getConnection()
    {
        Connection con = null;
        
       
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/datepersonale","root","");
            return con;
        } catch (SQLException ex) {
            Logger.getLogger(Gestiune.class.getName()).log(Level.SEVERE, null, ex);

            return null;
        }
       
    }
